#include "libft.h"

void			ft_lstadd(t_list **alst, t_list *new)
{
	if (!new && !alst)
		return ;
	if (!(*alst))
	{
		*alst = new;
		new->next = NULL;
	}
	else
	{
		new->next = *alst;
		*alst = new;
	}
}
