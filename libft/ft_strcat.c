#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	int i;
	int ct;

	i = 0;
	ct = -1;
	while (*(dst + i) != '\0')
		i++;
	while (*(src + (++ct)) != '\0')
		*(dst + (i++)) = *(src + ct);
	*(dst + i) = '\0';
	return (dst);
}
